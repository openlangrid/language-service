package jp.go.nict.langrid.wrapper.common.db.dao;

import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;
import org.apache.commons.lang.builder.ToStringBuilder;

/**
 * 
 * 
 * @author $Author: t-nakaguchi $
 * @version $Revision: 266 $
 */
public class BilingualDictionaryElement {

	public BilingualDictionaryElement() {
	}

	public BilingualDictionaryElement(String[] headWords) {
		this.headWords = headWords;
	}

	@Override
	public boolean equals(Object value){
		return EqualsBuilder.reflectionEquals(this, value);
	}

	@Override
	public int hashCode(){
		return HashCodeBuilder.reflectionHashCode(this);
	}

	@Override
	public String toString(){
		return ToStringBuilder.reflectionToString(this);
	}

	public String[] getHeadWords() {
		return headWords;
	}

	public void setHeadWords(String[] headWords) {
		this.headWords = headWords;
	}

	private String[] headWords;
}
